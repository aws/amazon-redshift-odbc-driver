#include "IAMCredentials.h"

using namespace Redshift::IamSupport;
using namespace Aws::Auth;

////////////////////////////////////////////////////////////////////////////////////////////////////
IAMCredentials::IAMCredentials(
    const rs_string& in_accessId,
    const rs_string& in_secretKey,
    const rs_string& in_sessionToken) :
    m_credentials(
    in_accessId, 
    in_secretKey,
    in_sessionToken),
    m_dbUser(),
    m_dbGroups(),
    m_forceLowercase(false),
    m_autoCreate(false)
{
    /* Do nothing */
}

////////////////////////////////////////////////////////////////////////////////////////////////////
IAMCredentials::IAMCredentials(
    const AWSCredentials& in_credentials) :
    m_credentials(in_credentials),
    m_dbUser(),
    m_dbGroups(),
    m_forceLowercase(false),
    m_autoCreate(false)
{
    /* Do nothing */
}

////////////////////////////////////////////////////////////////////////////////////////////////////
AWSCredentials IAMCredentials::GetAWSCredentials() const
{
    return m_credentials;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetAWSCredentials(const AWSCredentials& in_credentials)
{
    m_credentials = in_credentials;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
rs_string IAMCredentials::GetAccessId() const
{
    return m_credentials.GetAWSAccessKeyId();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetAccessId(const rs_string& in_accessId)
{
    m_credentials.SetAWSAccessKeyId(in_accessId);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
rs_string IAMCredentials::GetSecretKey() const
{
    return m_credentials.GetAWSSecretKey();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetSecretKey(const rs_string& in_secretKey)
{
    m_credentials.SetAWSSecretKey(in_secretKey);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
rs_string IAMCredentials::GetSessionToken() const
{
    return m_credentials.GetSessionToken();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetSessionToken(const rs_string& in_sessionToken)
{
    m_credentials.SetSessionToken(in_sessionToken);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
rs_string IAMCredentials::GetDbUser() const
{
    return m_dbUser;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetDbUser(const rs_string& in_dbUser)
{
    m_dbUser = in_dbUser;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
rs_string IAMCredentials::GetDbGroups() const
{
    return m_dbGroups;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetDbGroups(const rs_string& in_dbGroups)
{
    m_dbGroups = in_dbGroups;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
bool IAMCredentials::GetForceLowercase() const
{
    return m_forceLowercase;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetForceLowercase(bool in_forceLowercase)
{
    m_forceLowercase = in_forceLowercase;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
bool IAMCredentials::GetAutoCreate() const
{
    return m_autoCreate;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void IAMCredentials::SetAutoCreate(bool in_autoCreate)
{
    m_autoCreate = in_autoCreate;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
IAMCredentials::~IAMCredentials()
{
    /* Do nothing */
}
